class Clinitian {

    var clinitianName: String
    var medicalAssistantName: String
    var medicalAssistantPhoneNumber: String
    var flowCastNumber: String
    var location: String

    constructor(clinitianName: String, medicalAssistantName: String, medicalAssistantPhoneNumber: String, flowCastNumber: String, location: String){
        this.clinitianName = clinitianName
        this.medicalAssistantName = medicalAssistantName
        this.medicalAssistantPhoneNumber = medicalAssistantPhoneNumber
        this.flowCastNumber = flowCastNumber
        this.location = location
    }
}